	/**
	 * Internal to control word manager class.
	 * 
	 * @param kwd
	 * @param kwdbase
	 */
	private void add(String kwd, RtfCtrlWordBase kwdbase) {
		if(this.kwdTable.containsKey(kwd)) kwdTable.remove(kwd);
		this.kwdTable.put(kwd, kwdbase);
	}

	/**
	 * Internal to control word manager class.
	 * 
	 * @param kwdParam
	 * @param groupLevel
	 * @return
	 */
	public int handleKeyword(RtfCtrlWordData kwdParam, int groupLevel) {
		int result = RtfParser.errOK;
		// Call before handler event here
		result = dispatchKeyword(kwdParam, groupLevel);
		// call after handler event here
		return result;
	}
	
	private int dispatchKeyword(RtfCtrlWordData kwdParam, int groupLevel) {
		int result = RtfParser.errOK;
		
		if(kwdParam != null) {
			if(this.kwdTable.containsKey(kwdParam.ctrlWord))
			{
				result = RtfParser.errOK;
				RtfCtrlWordBase kwd = (RtfCtrlWordBase) this.kwdTable.get(kwdParam.ctrlWord);
				if(kwd != null) {
					kwd.handleControlword(kwdParam, groupLevel, this.reader);
				}
				
				if(debug && debugFound) {
					System.out.println("Keyword found:" +
						" New:" + kwdParam.ctrlWord + 
						" Param:" + kwdParam.param + 
						" bParam=" + String.valueOf(kwdParam.hasParam));
				}
			}
			else
			{
				result = RtfParser.errCtrlWordNotFound;
				//result = RtfParser2.errAssertion;
				if(debug && debugNotFound) {
					System.out.println("Keyword unknown:" + 
						" New:" + kwdParam.ctrlWord + 
						" Param:" + kwdParam.param + 
						" bParam=" + String.valueOf(kwdParam.hasParam));
				}
			}	
		}
		return result;
	}
